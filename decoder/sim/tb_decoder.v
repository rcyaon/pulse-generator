//========================================================================
// tb_decoder
//========================================================================

`timescale 1ns/1ps

module tb_decoder;

    //----------------------------------------------------------------------
    // Signals
    //----------------------------------------------------------------------

    reg  addr;
    wire wl0;
    wire wl1;

    //----------------------------------------------------------------------
    // Instantiate DUT
    //----------------------------------------------------------------------

    decoder dut (
        .addr (addr),
        .wl0  (wl0),
        .wl1  (wl1)
    );

    //----------------------------------------------------------------------
    // Main
    //----------------------------------------------------------------------

    initial begin
        $dumpfile("tb_decoder.vcd");
        $dumpvars(0, tb_decoder);

        addr = 0; #10;
        $display("ADDR=0: WL0=%b WL1=%b (expect 1 0)", wl0, wl1);

        addr = 1; #10;
        $display("ADDR=1: WL0=%b WL1=%b (expect 0 1)", wl0, wl1);

        $finish;
    end

endmodule